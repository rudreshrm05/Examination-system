    private void jButton6MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton6MouseClicked
        String sl=jComboBox1.getSelectedItem().toString();
        if(sl.equals("Results")){
             new Results(jTextField2.getText()).setVisible(true);
        }
        else if(sl.equals("Delete")){
             try{int f=0;
         for(int i=0;i<tida.length;i++){
            if(jTextField2.getText().equals(tida[i])){
                f=1;
            break;}
            }  
         if(f==1){
        PreparedStatement p=con.prepareStatement("delete from topic where topic_id='"+jTextField2.getText()+"';"); 
        p.execute();}
       else
       JOptionPane.showMessageDialog(null,"Please enter valid topic id");
       }
       catch(Exception e){
           System.out.println(e);
       }
       new CE_Topic().setVisible(true);
       this.setVisible(false);
        }
        else if(sl.equals("Modify")){
             new CE_ManageQ(jTextField2.getText()).setVisible(true);
        }
    }//GEN-LAST:event_jButton6MouseClicked

