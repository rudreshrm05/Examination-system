    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        
        int j=1;
         for(int i=0;i<tida.length;i++,j++){
            String s="TID"+j;
            if(s.equals(tida[i])==true){
                continue;
            }
            else
                tid=s;
            break;
            }  
         try{
             PreparedStatement p=con.prepareStatement("insert into topic values('"+tid+"','"+jTextField1.getText()+"');");
         p.executeUpdate();
         }
         catch(Exception e){
             System.out.println(e);
         }
         new CE_Qe(tid,jTextField1.getText()).setVisible(true);
         this.setVisible(false);
    }//GEN-LAST:event_jButton1MouseClicked

